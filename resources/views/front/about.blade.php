                                                <div class="col-lg-12">
                                                    T.C. Maltepe Üniversitesi Psikoloji bölümünü bitirmiştir. Lisans eğitimi sırasında, zorunlu stajını Marmara Eğitim ve Araştırma Hastanesinde tamamlamıştır. Marmara Eğitim ve Araştırma Hastanesi’nde psikiyatri kliniği, çocuk sağlığı ve psikiyatri servisi olmak üzere farklı birimlerde çalışmalarda bulunmuştur. Bu çalışmalar daha sonra Berlin’de devam etmiş ve Frei Universitat’ta psikoterapi eğitimine katılmıştır. Ve psikoterapist olarak kendini çok iyi bir şekilde geliştirmiştir. Klinik alanın yanı sıra Holdingler’ de çalışmalar yürütmüş ve Endüstriyel Psikoloji kapsamında da çalışmalarda bulunmuştur.
                                                    <br><br>
                                                    Birçok projede yer almıştır. Sokakta Yaşayan ve Çalışan Çocuklar projesi başta olmak üzere çeşitli kuruluşların ve belediyelerin projelerinde olarak yer almış ve çalışmalar yürütmüştür. Avrupa ve Türkiye arasında bir çok araştırma yapmış ve uluslarası literatüre giren birçok çalışmaya imza atmıştır. Birleşmiş Milletler projesi kapsamında savaş mağduru insanlar ve savaş stres reaksiyonu üzerine de çalışmalar yürütmektedir.
                                                    <br><br>
                                                    Lisans eğitimi sırasında Klinik Psikoloji Enstitüsünden Kişilik, Zeka, Gelişim testlerinin tümünün eğitimini uygulayıcı ehliyeti ile birlikte almıştır. Yine aynı zamanda Oyun terapisi eğitimi almıştır. Kurumsal danışmanlık açısından da ‘Yılın En İyi Kişisel Gelişim Eğitmeni’ seçilen Uzm. Psk. Erkan ÇİFTE’den Stratejik İnsan Kaynakları Yönetimi eğitimi ve Türkiyenin ilk endüstriyel psikoloğu ve İngiltere’de birçok başarılı projelere imza atan Prof. Nursel TELMAN‘ dan bizzat İş Psikolojisi, İnsan Kaynakları ve İnovasyon eğitimleri almıştır.
                                                    <br><br>
                                                    Çalışma hayatına başlayan M. Yunus ÇEKİÇ şirketlerde kurumsal danışmanlık ve psikolojik destek sağlarken, kendisini daha çok geliştirmek için Almanya’nın başkenti Berlin’e yerleşmiştir. Berlin’de Frei Universitat’ta eğitimlere devam ederken bir yandan Berlin’de projeler yürütmüş çeşitli kazanım ve katkılar sağlamıştır.
                                                    <br><br>
                                                    Berlin’de dil eğitimi ile beraber Holdinglerde endüstriyel psikolog olarak çalışmalar yapmıştır. Amerika ve Almanya arasında büyük enerji şirketlerine danışmanlık yapan ve Freie Universitat öğretim görevlisi Dr. Martin RİEDEL ile birlikte bir çok projede yer almış çeşitli kazanımlar sağlamıştır. Kurumsal Danışmalığın yanı sıra Germedica Berlin kliniğinde danışanlarına hizmet sunmuştur.
                                                    <br><br>
                                                    Berlin’deki Türk psikologlar ve Büyükelçilik yetkilileri ile birlikte vakıf çalışmalarında bulunmuş ve kurucu üye olarak yer almıştır. Yine bu süreçte Berlin temaslarında ‘Netzwerk Europäisch-Türkischer Unternehmen’ (Avrupalı Türk İş Adamları Kuruluşu) üyesi olmuş ve o ailenin bir parçası olarak Netu Akademi üzerinden seminerler ve eğitimler gerçekleştirmiştir. Halen Netu üyesi olarak hizmetlerine devam etmektedir.
                                                    <br><br>
                                                    Bunların yanı sıra İstanbul’da da His Danışmanlık Merkezi ve Akademi olarak ofisini kuran M. Yunus Çekiç önemli işlere imza atmıştır. Turkcell, LG, Pronet vb. büyük firmalar ile anlaşmalar sağlamış ve çözüm ortağı olarak endüstriyel psikoloji kapsamında katkı ve hizmetlerde bulunmuş ve bulunmaya devam etmektedir.
                                                    <br><br>
                                                    Yine Berlin’deki süreçte ‚İş ve Hayat dengesi‘ eğitimi almış bulunmaktadır. Bu süreçte Maltepe Üniversitesi Endüstriyel ve Örgütsel Psikoloji yüksek Lisans Programında uzmanlık çalışmalarına dahil etmiştir. ‘Almanya ve Türkiye’de Çalışan ve Yaşayan Psikologların Sosyolojik ve Psikoloji Açıdan Karşılaştırılmasına yönelik tez çalışması bulunmaktadır. 2018 – 2019 ‘Yılın En Başarılı Psikoloğu’ ödülüne layık görülmüştür. Halen bir çok gazete ve dergide köşe yazarlığı yapmakta çeşitli mecralarda röportajlar vermektedir.
